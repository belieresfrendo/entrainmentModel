%===============================================================================
%                           INITIAL CONDITIONS
%===============================================================================
function ic = initialConditions(xv)
  ic = zeros([numel(xv),2]);
  ic(:,1) = max(1.0 - ((xv-50.0)/20.0).^2, 0.00001);
  ic(:,2) = 0.0;					% h*u = 0 => u = 0 for all r
end

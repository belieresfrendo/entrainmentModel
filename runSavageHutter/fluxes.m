%===============================================================================
%                             FLUX FUNCTION F
%===============================================================================
function flux = fluxes(uv, cosTheta)
  global g;
  % uv = (h, hu)
  flux = [uv(:,2) ...
          uv(:,2).^2 ./ uv(:,1) + 0.5*g*cosTheta*uv(:,1).^2];
end